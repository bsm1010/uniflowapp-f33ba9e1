/**
 * Add a marketplace product to the current reseller's store.
 * Creates (or upserts) a row in reseller_listings with selling_price.
 *
 * The DB trigger trg_reseller_listings_price_check rejects selling_price
 * below the product's platform_price — we pre-check here for a friendlier
 * error message.
 */
export function useAddToMyStore() {
  const qc = useQueryClient();
  const { user } = useAuth();
  return useMutation({
    mutationFn: async (args: {
      marketplaceProductId: string;
      sellingPrice: number;
      storeId?: string | null;
    }) => {
      if (!user) throw new Error("Not signed in");
      if (args.sellingPrice < 0) throw new Error("sellingPrice must be >= 0");

      // Friendly pre-check (DB trigger is the real safety net).
      const { data: product, error: pErr } = await supabase
        .from("marketplace_products")
        .select("platform_price, availability_status")
        .eq("id", args.marketplaceProductId)
        .maybeSingle();
      if (pErr) throw new Error(pErr.message);
      if (!product) throw new Error("Marketplace product not found");
      if (product.availability_status !== "available") {
        throw new Error("This product is currently unavailable");
      }
      if (args.sellingPrice < Number(product.platform_price)) {
        throw new Error(`sellingPrice must be >= platform_price (${product.platform_price})`);
      }

      const { data, error } = await supabase
        .from("reseller_listings")
        .upsert(
          {
            reseller_id: user.id,
            marketplace_product_id: args.marketplaceProductId,
            store_id: args.storeId ?? null,
            selling_price: args.sellingPrice,
            is_active: true,
          },
          { onConflict: "reseller_id,marketplace_product_id" },
        )
        .select(
          "id, reseller_id, marketplace_product_id, store_id, selling_price, is_active, total_orders, total_returns, total_profit_earned, created_at",
        )
        .single();
      if (error) throw new Error(error.message);
      return data as ResellerListing;
    },
    onSuccess: () => {
      qc.invalidateQueries({ queryKey: dropshippingKeys.myListings(user?.id) });
    },
  });
}

/**
 * Confirm + pay for an existing pending dropship order.
 *
 * The order must already exist with status='pending_payment' (created by
 * the storefront checkout via `create_pending_dropship_order`). This hook
 * debits the wallet atomically and flips the order to 'paid_by_reseller'.
 * Insufficient balance → RPC raises P0001 with a clear message.
 */
export function useConfirmAndPayOrder() {
  const qc = useQueryClient();
  const { user } = useAuth();
  return useMutation({
    mutationFn: async (args: { orderId: string }) => {
      if (!user) throw new Error("Not signed in");
      try {
        const { error } = await supabase.rpc("confirm_and_pay_order", {
          p_order_id: args.orderId,
        });
        if (error) throw new Error(error.message);
        return args.orderId;
      } catch (e) {
        console.error("[useConfirmAndPayOrder] RPC error:", e instanceof Error ? e.message : e);
        throw e;
      }
    },
    onSuccess: () => {
      qc.invalidateQueries({ queryKey: dropshippingKeys.myOrders(user?.id) });
      qc.invalidateQueries({ queryKey: dropshippingKeys.myWallet(user?.id) });
      qc.invalidateQueries({ queryKey: dropshippingKeys.myWalletTx(user?.id) });
      qc.invalidateQueries({ queryKey: dropshippingKeys.myListings(user?.id) });
    },
  });
}

