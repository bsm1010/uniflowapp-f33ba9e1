/**
 * Browse the marketplace catalog.
 *
 * Filters:
 *  - category: exact match
 *  - maxReturnRate: only products with return_rate_percent <= this (0-100)
 *
 * Returns the row columns the marketplace RLS policy exposes to resellers
 * (cost_price is column-revoked server-side, so it's never returned).
 */
export function useMarketplaceProducts(filters?: {
  category?: string;
  maxReturnRate?: number;
}): UseQueryResult<MarketplaceProductWithJoins[]> {
  const { user } = useAuth();
  return useQuery({
    queryKey: dropshippingKeys.products(filters),
    enabled: !!user,
    queryFn: async () => {
      let q = supabase
        .from("marketplace_products")
        .select(
          "id, name, description, images, category, supplier_name, supplier_wilaya, " +
            "platform_price, availability_status, return_rate_percent, total_orders_count, " +
            "created_at, updated_at",
        )
        .eq("availability_status", "available")
        .order("created_at", { ascending: false });
      if (filters?.category) q = q.eq("category", filters.category);
      if (typeof filters?.maxReturnRate === "number") {
        q = q.lte("return_rate_percent", filters.maxReturnRate);
      }
      const { data, error } = await q;
      if (error) {
        console.error("[useMarketplaceProducts] Query error:", error.message);
        return [] as MarketplaceProductWithJoins[];
      }
      return (data ?? []) as unknown as MarketplaceProductWithJoins[];
    },
  });
}

